
namespace Anycmd.Storage
{
    /// <summary>
    /// 表示排序模式。
    /// </summary>
    public enum SortOrder
    {
        /// <summary>
        /// 表示未指定的排序模式。
        /// </summary>
        Unspecified = -1,
        /// <summary>
        /// 表示升序。
        /// </summary>
        Ascending = 0,
        /// <summary>
        /// 表示降序。
        /// </summary>
        Descending = 1
    }
}
